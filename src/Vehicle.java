public class Vehicle {
    int no_tires;
    int no_seat;

    public Vehicle(int no_tires, int no_seat) {
        this.no_tires = no_tires;
        this.no_seat = no_seat;
    }

    public int getNo_tires() {

        return no_tires;
    }

    public int getNo_seat() {
        return no_seat;
    }
}
